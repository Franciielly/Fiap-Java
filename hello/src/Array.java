public class Array {
    public static void main(String[] args) {
        float valores[] = new float [5];
        valores[0] = 10;
        valores[1] = 20;
        valores[2] = 30;
        valores[3] = 40;
        valores[4] = 50;

        System.out.println(valores[3]);

        float [] valores1 = { 10 , 20 , 30 , 40};
        float  [] valores2 = new float [] { 10 , 20 , 30, 40 };

        System.out.println(valores1[1]);
        System.out.println(valores2[2]);

    }
}
